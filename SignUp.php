    document.addEventListener("DOMContentLoaded", function() {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    const passwordPattern = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)[A-Za-z\d]{8,}$/;

    const registerForm = document.getElementById("register-form");


    function getQueryParam(param) {
        const params = new URLSearchParams(window.location.search);
        return params.get(param);
    }

    if (getQueryParam("emailExists") === "true") {
        const emailField = document.getElementById("email");
        if (emailField) {
            const errorMessageDiv = document.createElement("div");
            errorMessageDiv.textContent = "Email is already registered. Please use a different email address.";
            errorMessageDiv.id = "emailError";
            errorMessageDiv.style.color = "red";
            emailField.parentNode.appendChild(errorMessageDiv);
            emailField.classList.add("error");

            emailField.addEventListener("input", () => {
                const errorDiv = document.getElementById("emailError");
                if (errorDiv) {
                    errorDiv.remove();
                    emailField.classList.remove("error");
                }
            });
        }
    }


    function validateForm(e) {
        const form = e.target;
        const requiredFields = form.querySelectorAll("[required]");
        const passwordField = form.querySelector("input[name='password']");
        const confirmPasswordField = form.querySelector("input[name='re_pass']");

        let hasError = false;

        requiredFields.forEach((field) => {
            const errorMessageDiv = field.parentNode.querySelector(".error-message") || document.createElement("div");
            errorMessageDiv.className = "error-message";
            field.parentNode.appendChild(errorMessageDiv);

            if (!field.value.trim()) {
                errorMessageDiv.textContent = "This field is required.";
                field.classList.add("error");
                hasError = true;
            }

            if (field.type === "email" && !emailRegex.test(field.value.trim())) {
                errorMessageDiv.textContent = "Please enter a valid email address.";
                field.classList.add("error");
                hasError = true;
            }

       
            field.addEventListener("input", () => {
                errorMessageDiv.textContent = "";
                field.classList.remove("error");
            });
        });

        if (!passwordPattern.test(passwordField.value.trim())) {
            const errorMessageDiv = passwordField.parentNode.querySelector(".error-message") || document.createElement("div");
            errorMessageDiv.className = "error-message";
            passwordField.parentNode.appendChild(errorMessageDiv);
            errorMessageDiv.textContent = "Password must be at least 8 characters with uppercase, lowercase, and numbers.";
            passwordField.classList.add("error");
            hasError = true;
        }

        if (passwordField.value.trim() !== confirmPasswordField.value.trim()) {
            const errorMessageDiv = confirmPasswordField.parentNode.querySelector(".error-message") || document.createElement("div");
            errorMessageDiv.className = "error-message";
            confirmPasswordField.parentNode.appendChild(errorMessageDiv);
            errorMessageDiv.textContent = "Passwords do not match.";
            confirmPasswordField.classList.add("error");
            hasError = true;
        }

        if (hasError) {
            e.preventDefault(); 
        }
    }

   
    if (registerForm) {
        registerForm.addEventListener("submit", function(e) {
            validateForm(e);

            if (!e.defaultPrevented) {
                const formData = new FormData(registerForm);
                fetch("insert.php", {
                    method: "POST",
                    body: formData,
                })
                .then((response) => response.json())
                .then((data) => {
                    console.log("Form submitted successfully:", data);
                
                })
                .catch((error) => {
                    console.error("Fetch error:", error);
                });
            }
        });
    }
});



